
/**
 * Pivot class for Median of Three Partitioning algorithm
 * @author Wooseok Kim
 * 
 */
public class MedianOfThreePivot {
	private int i;
	private int j;
	
	/**
	 * @param i
	 * @param j
	 */
	public MedianOfThreePivot(int i, int j){
		this.i = i;
		this.j = j;
	}
	
	/**
	 * @return i
	 */
	public int getI() {
		return i;
	}
	
	/**
	 * @return j
	 */
	public int getJ(){
		return j;
	}
}
